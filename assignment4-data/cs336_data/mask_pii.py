# cs336_data/mask_pii.py
import re

# ─────────────────────────────────────────
# 1. 邮件地址
# ─────────────────────────────────────────
# 邮件格式: 本地部分 @ 域名.顶级域名
# 本地部分: 字母数字 + 特殊字符(._%+-)
# 域名: 字母数字 + 连字符
# 顶级域名: 2-6个字母

EMAIL_PATTERN = re.compile(
    r"""
    [a-zA-Z0-9._%+\-]+   # 本地部分：字母数字和特殊字符
    @                     # @符号
    [a-zA-Z0-9.\-]+       # 域名
    \.                    # 点
    [a-zA-Z]{2,6}         # 顶级域名（com/org/edu等）
    """,
    re.VERBOSE,
)


def mask_emails(text: str) -> tuple[str, int]:
    """
    替换文本中所有邮件地址。
    返回: (处理后的文本, 替换次数)
    """
    result, count = re.subn(EMAIL_PATTERN, "|||EMAIL_ADDRESS|||", text)
    return result, count


# ─────────────────────────────────────────
# 2. 电话号码
# ─────────────────────────────────────────
# 美国电话格式多样，常见的有：
#   (123) 456-7890
#   123-456-7890
#   123.456.7890
#   +1 123 456 7890
#   1234567890（纯数字）

PHONE_PATTERN = re.compile(
    r"""
    (?<!\d)               # 左边不能是数字（防止匹配更长数字串的一部分）
    (?:
        (?:\+?1[\s.\-]?)? # 可选的国家代码 +1 或 1
        (?:
            \(\d{3}\)     # (123) 带括号区号
            |
            \d{3}         # 123  不带括号区号
        )
        [\s.\-]?          # 分隔符（空格、点、连字符）可选
        \d{3}             # 局号3位
        [\s.\-]?          # 分隔符
        \d{4}             # 用户号4位
    )
    (?!\d)                # 右边不能是数字
    """,
    re.VERBOSE,
)


def mask_phone_numbers(text: str) -> tuple[str, int]:
    """
    替换文本中所有电话号码。
    返回: (处理后的文本, 替换次数)
    """
    result, count = re.subn(PHONE_PATTERN, "|||PHONE_NUMBER|||", text)
    return result, count


# ─────────────────────────────────────────
# 3. IP地址（IPv4）
# ─────────────────────────────────────────
# IPv4格式: 0-255 . 0-255 . 0-255 . 0-255
# 难点: 要确保每段不超过255，且不匹配版本号如"1.2.3.4.5"

IP_PATTERN = re.compile(
    r"""
    (?<!\d\.)(?<!\d)      # 左边不能是"数字."或数字
    (?:
        (?:25[0-5]|       # 250-255
           2[0-4]\d|      # 200-249
           1\d{2}|        # 100-199
           [1-9]?\d)      # 0-99
        \.
    ){3}                  # 前三段重复3次
    (?:25[0-5]|2[0-4]\d|1\d{2}|[1-9]?\d)  # 最后一段
    (?!\d|\.\d)           # 右边不能是数字或".数字"
    """,
    re.VERBOSE,
)


def mask_ips(text: str) -> tuple[str, int]:
    """
    替换文本中所有IPv4地址。
    返回: (处理后的文本, 替换次数)
    """
    result, count = re.subn(IP_PATTERN, "|||IP_ADDRESS|||", text)
    return result, count
